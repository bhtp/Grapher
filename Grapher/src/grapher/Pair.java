package grapher;

/**
 *
 * @author Parker
 */
public class Pair {
    public int x;
    public int y;
    /**
     * pairs an x and a y value
     * @param x x value
     * @param y y value
     */
    public Pair(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    
}
